public class ArrDefine {
    public static void main(String[] args) {
        // 배열 선언 및 초기화
        int[] score = {70, 80, 90}; // 정수 배열
        String[] names = {"홍길동", "고길동", "박길동"}; // 문자열 배열

        // 배열의 값에 접근하고 출력
        System.out.println("첫 번째 점수: " + score[0]); // 70 출력
        System.out.println("두 번째 이름: " + names[1]); // 고길동 출력
    }    
}
